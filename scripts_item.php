                  <p class="center">Примечание переводчика (словарь терминов).</p>
                  <p>Те из читателей кто хорошо разбирается в несложной терминологии сценарного языка, могут спокойно пролистнуть эту страницу. Остальным же это поможет более полно понять сценарий – увидеть фильм своими глазами с листа бумаги, как это делает режиссёр.</p>
                  <p>Я хочу познакомить вас всего лишь с коротким списком сокращений на английском языке и о том, как они переведены мной на русский, а также с особенностями данного конкретного сценария. Итак, в процессе чтения вам встретятся некоторые сокращения, возможно неясного значения, вот их расшифровка:</p>
                  <p>INT. или EXT. - ИНТ. или ЭКСТ. – это, я думаю, самое простое, обозначает, где происходит действие в помещении или вне его. INTERIOR – внутри, EXTERIOR – снаружи.</p>
                  <p>CU : КП – Close Up : Крупный План – наиболее часто применяемый кино ход.</p>
                  <p>Далее идут два термина, которые очень важно не спутать:</p>
                  <p>VO : ЗК – Voice Over : За Кадром. Это также характеристика реплики, но это комментарий к изображению – произносящий его не находиться на месте действия.</p>
                  <p>POV : ТЗ – Point Of View : Точка Зрения. Мы видим сцену глазами одного из персонажей. С его точки зрения. Встречается не часто.</p>
                  <p>PAN : ПАН – Panorama : Панорама. Все просто. Движение камеры, обычно на 180 градусов. Если нет, – то градусная мера указывается дополнительно.</p>
                  <p>Вот, наверное, и все, хотя есть одно сокращение, которое я перевёл не совсем, так как надо, это DOLLY – обозначает тележку на рельсах, на которую устанавливается камера, вы могли видеть её в репортажах о съёмках какого-либо фильма. Я перевел это просто как КАМЕРА. Потому что это тонкости, которые сценарист описывает для оператора. Например:</p>
                  <p>DOLLY slowly into C.U.  – СРЕДНИЙ план медленно переходящий в КРУПНЫЙ. (Так я написал в переводе).На самом деле, это означает следующее: Крупный план осуществляется путём передвижения камерыпо рельсам ближе к персонажу, а не путём оптики. Но думаю без этой тонкости можно обойтись.</p>
