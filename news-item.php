                <p>На сайте <a href="#"><b>Change.org</b></a> собирают голоса под петицией о <b>закрытии сайта Rotten Tomatoes</b>. Причиной обструкции стали низкие оценки критиков, выставленные новому фильму киновселенной DC Comics «<a href="#"><b>Отряд самоубийц</b></a>». </p>
                <p>Сайт <i>Rotten Tomatoes</i> генерирует среднюю оценку фильма на основе критических рецензий в североамериканской прессе. К разочарованию фанатов комикс- франшизы «Отряд самоубийц» получил лишь 31% положительных рецензий (на топ-критиков приходится и вовсе 22%).  </p>
                <p>По горячим следам разгрома на Change.org появилась страничка с призывом закрыть неугодный сайт (который сам не выносит никаких вердиктов, а лишь выводит среднее арифметической из оценок критиков): «Мы требуем закрыть сайт, потому что критики всегда пишут на фильмы киновселенной DC Comics несправедливые, разгромные рецензии. Они влияют на мнение людей, даже если это по-настоящему великолепное кино». </p>
                <p>Позже на страничке появилась еще одна ремарка, снижающая градус возмущения: <i>«С помощью петиции сайт не закрыть. Ее цель — сообщить критикам о том, что есть много людей, не согласных с их оценкой»</i>. </p>
                <p>Вчера автор текста — некий Абдулла Колдуотер — закрыл страничку, опубликовав финальное заявление. «На самом деле я запустил петицию по приколу, для того, чтобы фанаты киновселенной DC выразили свой гнев. Не относитесь к этому серьезно. Я подумал и понял, что это бессмысленно. Будет много гневных постов и перепалок между поклонниками и противниками. Кино должно приносить удовольствие. А ненависть и брань — это нечто противоположное». На момент закрытия петиция собрала 17614 подписей. </p>
                <p>По прогнозам, разгром критиками «Отряда самоубийц» не помешает фильму заработать в первый уикенд американского проката от 125 до 140 млн долларов. В Америке фильм выходит завтра, 5 августа. Россия встречает картину Дэвида Эйра уже сегодня.</p>
